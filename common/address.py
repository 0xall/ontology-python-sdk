ADDR_LEN = 20  # the size of address should be 20 bytes


def address_parse_from_bytes(addr):
    '''
    :param addr: []byte
    :return: [20]byte
    '''
    if len(addr) != ADDR_LEN:
        raise ValueError("[common]: address_parse_from_bytes error, len != 20")
    return addr


